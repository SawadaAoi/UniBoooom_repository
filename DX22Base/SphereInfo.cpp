/* ========================================
関数：Angle
----------------------------------------
内容：引数との角度を取得
----------------------------------------
引数1：測りたい相手のSphere構造体
----------------------------------------
戻値：角度
======================================== */
float tagSphereInfo::Angle(tagSphereInfo& Obj)
{
	return atan2((Obj.fPos.z - this->fPos.z) , (Obj.fPos.x - this->fPos.x));
}