{
}

/* ========================================
	タイムスタート待つ関数
	----------------------------------------
	内容：タイマーを動く前の待つ時間設定
	----------------------------------------
	引数1：待つ時間（秒）
	----------------------------------------
	戻値：なし
=========================================== */
void CTimer::TimeStart(float maxTime)
{
	m_nTimeCnt = maxTime * 60;
	m_bStartFlg = true;
}

void CTimer::TimeStart(float maxTime, float waitTime)
{
	m_dWaitCnt = waitTime * 60;
}

void CTimer::WaitTimeCheck()
{
	m_dWaitCnt--;
	// 0になったらタイムカウント開始
	if (m_dWaitCnt <= 0)
	{
		m_bStartFlg = true;
	}

}
