/* ========================================
	タイマー取得関数
	----------------------------------------
	内容：タイマーの分計算
	----------------------------------------
	引数1：なし
	----------------------------------------
	戻値：時間の分
=========================================== */
int CTimer::GetMinite()
{
	m_nTimeCnt = m_nTimeCnt / 3600;			//分 = 総タイム割る（60秒 * 60フレーム）

	return m_nTimeCnt;
}
/* ========================================
	タイマー取得関数
	----------------------------------------
	内容：タイマーの秒数計算
	----------------------------------------
	引数1：なし
	----------------------------------------
	戻値：時間の秒
=========================================== */
int CTimer::GetSecond()
{
	m_nTimeCnt = ( m_nTimeCnt / 60 ) % 60;	//秒 = 分の部分を抜くの残り

	return m_nTimeCnt;
}

